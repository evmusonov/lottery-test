<?php

namespace Casino\Domain\Entity;

class Item extends AbstractEntity
{
    protected $name;
    protected $count;

    public function getName()
    {
        return $this->name;
    }

    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }

    public function getCount()
    {
        return $this->count;
    }

    public function setCount($count)
    {
        $this->count = $count;
        return $this;
    }
}